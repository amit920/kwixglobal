        <script type="text/javascript">
            var offsetTime = "+5.5"
            $('.countryimg').mouseover(function () {
                $('.countryimg').removeClass('overdata')
                var data = $(this).find('.getdata').html()
                $('.contryaddress').html(data)
                offsetTime = $(this).data('offset')
                $(this).addClass('overdata')

            })
            setInterval(function () {
                getTime()
            }, 1000);
            function getTime() {
                d = new Date();
                utc = d.getTime() + (d.getTimezoneOffset() * 60000);
                date = new Date(utc + (3600000 * offsetTime));
                updateClock(date);
            }
            function updateClock(date) {
                var secHand = document.getElementById("sec").style;
                var minHand = document.getElementById("min").style;
                var hrHand = document.getElementById("hour").style;
                secHand.transform = "rotate(" + date.getSeconds() * 6 + "deg)";
                minHand.transform = "rotate(" + date.getMinutes() * 6 + "deg)";
                hrHand.transform = "rotate(" + (date.getHours() * 30 + date.getMinutes() * 0.5) + "deg)";
            }
